import math, os
from math import pi, fabs, ceil, floor 
from os import system 

system('clear')

# function round()
print('---- round() function ----')
print('The value of pi is', round(pi, 2))
print('--------------------------')
print()

number = -1
# function fabs()
print('---- fabs() function ----')
print('The absolute value of', number, 'is', fabs(number))
print('--------------------------')
print()

# floor function
number1 = 3.7
print('---- floor() function ----')
print('The floor of', number1, 'is', floor(number1))
print('--------------------------')
print()

# ceil function
print('---- ceil() function ----')
print('The ceiling of', number1, 'is', ceil(number1))
print('--------------------------')
print()